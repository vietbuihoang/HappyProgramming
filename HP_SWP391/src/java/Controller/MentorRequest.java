    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        DAO dao = new DAO();

        HttpSession session = request.getSession();
        User u = (User) session.getAttribute("active");
        int menteeID = u.getUserID();
        int mentorID = 0;
        
        for(MentorProfile mp: dao.getMentorProfile()){
            if(mp.getUserID()==menteeID){
                mentorID = mp.getMentorID();
            }
        }

        ArrayList<Request> reqList = new ArrayList<>();
        for (Request r : dao.getRequest()) {
            if (r.getMentorID()== mentorID && r.getStatus()==2) {
                reqList.add(r);
            }
        }
        
        request.setAttribute("size", reqList.size());
        request.setAttribute("reqList", reqList);
        request.setAttribute("listRS", dao.getRequestSkill());
        request.setAttribute("listS", dao.getSkillCategory());
        request.setAttribute("listU", dao.getUser());
        request.getRequestDispatcher("MentorRequest.jsp").forward(request, response);
    }

